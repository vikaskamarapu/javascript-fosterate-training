let a = 5;

// equal to operator
console.log(a == 5);
console.log(a == '5');

a = 3;
let b = 'hello';

// not equal operator
console.log(a != 2);
console.log(b != 'Hello');

a = 2;

// strict equal operator
console.log(a === 2);
console.log(a === '2');

a = true;
b = false;

// logical AND
console.log(a && a);
console.log(a && b);

// logical OR
console.log(a || b);
console.log(b || b);

// logical NOT
console.log(!a);
console.log(!b); 